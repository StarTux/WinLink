package com.winthier.winlink;

public enum DisconnectCause {
        READ_TIMEOUT("Read timeout"),
        IO_ERROR("I/O Error"),
        SHUTDOWN("Shut down"),
        OTHER("Unknown cause");

        public final String message;
        DisconnectCause(String message) {
                this.message = message;
        }
        @Override
        public String toString() {
                return message;
        }
}
